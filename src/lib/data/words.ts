/**
 * Get a random word from an array
 *
 * Uses crypto.getRandomValues for true randomness if available,
 * falls back to Math.random() if not.
 *
 * @param array - Array of words to choose from
 * @returns Random word from array
 *
 * @example
 * ```typescript
 * const adj = getRandomWord(ADJECTIVES);
 * console.log(adj); // "Electric"
 * ```
 */
export function getRandomWord(array: string[]): string {
	const index = Math.floor(Math.random() * array.length);
	return array[index];
}

/**
 * Get multiple random words from an array without duplicates
 *
 * @param array - Array of words to choose from
 * @param count - Number of words to get
 * @returns Array of unique random words
 *
 * @example
 * ```typescript
 * const adjs = getRandomWords(ADJECTIVES, 3);
 * console.log(adjs); // ["Electric", "Neon", "Cosmic"]
 * ```
 */
export function getRandomWords(array: string[], count: number): string[] {
	// Shuffle array and take first N elements
	const shuffled = [...array].sort(() => Math.random() - 0.5);
	return shuffled.slice(0, Math.min(count, array.length));
}

